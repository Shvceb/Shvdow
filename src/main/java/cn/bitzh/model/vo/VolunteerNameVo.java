package cn.bitzh.model.vo;

public class VolunteerNameVo {
	private String studentId;

	private String studentName;

	private String firstVolunteerName;

	private String secondVolunteerName;

	private String thirdVolunteerName;

	public String getStudentId() {
		return studentId;
	}

	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public String getFirstVolunteerName() {
		return firstVolunteerName;
	}

	public void setFirstVolunteerName(String firstVolunteerName) {
		this.firstVolunteerName = firstVolunteerName;
	}

	public String getSecondVolunteerName() {
		return secondVolunteerName;
	}

	public void setSecondVolunteerName(String secondVolunteerName) {
		this.secondVolunteerName = secondVolunteerName;
	}

	public String getThirdVolunteerName() {
		return thirdVolunteerName;
	}

	public void setThirdVolunteerName(String thirdVolunteerName) {
		this.thirdVolunteerName = thirdVolunteerName;
	}

}
